package main

import (
	"log"
	"os"
	"text/template"

	"github.com/Xuanwo/storage/types"
	"github.com/Xuanwo/templateutils"
)

func main() {
	const filePath = "metadata.go"

	f, err := os.Create(filePath)
	if err != nil {
		log.Fatal(err)
	}
	defer f.Close()

	err = tmpl.Execute(f, struct {
		Data map[string]string
	}{
		types.AvailablePairs,
	})
	if err != nil {
		log.Fatal(err)
	}
}

var tmpl = template.Must(template.New("").Funcs(templateutils.FuncMap()).Parse(`// Code generated by go generate internal/cmd/metdata_gen; DO NOT EDIT.
package types

import (
	"time"
)

{{- range $k, $v := .Data }}
// Get{{ $k | camelCase }} will get {{ $k }} value from metadata.
func (m Metadata) Get{{ $k | camelCase }}() ({{ $v }}, bool)  {
	v, ok := m[{{ $k | camelCase }}]
	if !ok {
		return {{ $v | zeroValue }}, false
	}
	return v.({{ $v }}), true
}

// Set{{ $k | camelCase }} will set {{ $k }} value into metadata.
func (m Metadata) Set{{ $k | camelCase }}(v {{ $v }})  {
	m[{{ $k | camelCase }}] = v
}
{{- end }}
`))
